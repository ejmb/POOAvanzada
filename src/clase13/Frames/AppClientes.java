    private void tClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tClientesMouseClicked
        int fila = tClientes.getSelectedRow();
        if(fila >= 0){
            Cliente cliente = clientes.get(fila);
            txtCodigo.setText(Integer.toString(cliente.getCodigo()));
            txtNombre.setText(cliente.getNombre());
            fTxtRuc.setText(cliente.getRuc());
            txtDireccion.setText(cliente.getDireccion());
            fTxtDesc.setText(Float.toString(cliente.getDescuento()));
            
            enableFields();
            bModificar.setEnabled(true);
            bEliminar.setEnabled(true);
        }
    }//GEN-LAST:event_tClientesMouseClicked

    private void bModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bModificarActionPerformed
        Cliente cliente = new Cliente();
        Number descuento = (Number)fTxtDesc.getValue();
        cliente.setCodigo(Integer.parseInt(txtCodigo.getText()));
        cliente.setRuc(fTxtRuc.getText());
        cliente.setNombre(txtNombre.getText());
        cliente.setDireccion(txtDireccion.getText());
        cliente.setDescuento(descuento.floatValue());
        
        int fila = tClientes.getSelectedRow();
        if(fila >= 0){
            validateFields();    
            clientes.get(fila).setCodigo(cliente.getCodigo()); 
            clientes.get(fila).setNombre(cliente.getNombre());
            clientes.get(fila).setRuc(cliente.getRuc());
            clientes.get(fila).setDireccion(cliente.getDireccion());            
            clientes.get(fila).setDescuento(cliente.getDescuento());

            tClientes.setValueAt(cliente.getCodigo(), fila, 0);
            tClientes.setValueAt(cliente.getNombre(), fila, 1);
            tClientes.setValueAt(cliente.getRuc(), fila, 2);
            tClientes.setValueAt(cliente.getDireccion(), fila, 3);
            tClientes.setValueAt(cliente.getDescuento(), fila, 4);

            cleanFields();
            disableFields();
            bModificar.setEnabled(false);
            bEliminar.setEnabled(false);
        }        
    }//GEN-LAST:event_bModificarActionPerformed

    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
        int row = tClientes.getSelectedRow();
        if(row >= 0){
            int option = JOptionPane.showConfirmDialog(this, "Est√° seguro de eliminar?", 
                    "Eliminar Cliente", JOptionPane.YES_NO_OPTION);
            if(option == JOptionPane.YES_OPTION){
                clientes.remove(row);
                modelo.removeRow(row);
                cleanFields();
                disableFields();
                bModificar.setEnabled(false);
                bEliminar.setEnabled(false);                
            }
        }
    }//GEN-LAST:event_bEliminarActionPerformed


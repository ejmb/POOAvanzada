    private int getNewCod(){
        cod++;        
        return cod;
    }
    
    //Cargar JTable
    private void loadClientesJtable() {
        modelo = new DefaultTableModel();
        String nombresCol[] = {"Codigo", "Nombre", "RUC", "Direccion", "Descuento"};
        modelo.setColumnIdentifiers(nombresCol);
        tClientes.setModel(modelo); 
        tClientes.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
    }
    
    //Agregar Cliente al Vector
    private void addCliente(){
        Cliente cliente = new Cliente(); 
        Number descuento = (Number)fTxtDesc.getValue();
        cliente.setCodigo(Integer.parseInt(txtCodigo.getText()));
        cliente.setRuc(fTxtRuc.getText());
        cliente.setNombre(txtNombre.getText());
        cliente.setDireccion(txtDireccion.getText());
        cliente.setDescuento(descuento.floatValue());        
        
        clientes.add(cliente);
        Object[] fila = { cliente.getCodigo(), cliente.getNombre(), cliente.getRuc(), 
            cliente.getDireccion(), cliente.getDescuento()};
        modelo.addRow(fila);
    }

    //Valida los Campos
    private boolean validateFields() {        
        //Validar RUC vacio
        if(fTxtRuc.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Campo RUC no puede estar VACIO",
                    "Error:", JOptionPane.ERROR_MESSAGE);
            fTxtRuc.requestFocus();
            return false;           
        }
        //Validar Nombre vacio
        if(txtNombre.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Campo NOMBRE no puede estar VACIO",
                    "Error:", JOptionPane.ERROR_MESSAGE);
            txtNombre.requestFocus();
            return false;           
        }
        //Validar Direccion vacia
        if(txtDireccion.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Campo DIRECCION no puede estar VACIO",
                    "Error:", JOptionPane.ERROR_MESSAGE);
            txtDireccion.requestFocus();
            return false;           
        }
        //Validar Descuento        
        if(fTxtDesc.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Campo DESCUENTO no puede estar VACIO",
                    "Error:", JOptionPane.ERROR_MESSAGE);
            fTxtDesc.requestFocus();
            return false;           
        }
        Number value = (Number)fTxtDesc.getValue();
        if(value.floatValue()<0 || value.floatValue()>30){
            JOptionPane.showMessageDialog(this, "Campo DESCUENTO Fuera de Rango [0 - 30]%",
                    "Error:", JOptionPane.ERROR_MESSAGE);
            fTxtDesc.requestFocus();
            return false;
        }
        return true;
    }
    
